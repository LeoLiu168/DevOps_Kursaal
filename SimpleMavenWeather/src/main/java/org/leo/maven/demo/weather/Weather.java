package org.leo.maven.demo.weather;

public class Weather {
	
	/**
	 * @uml.property  name="city"
	 */
	private String city;
	
	/**
	 * @uml.property  name="region"
	 */
	private String region;
	
	/**
	 * @uml.property  name="country"
	 */
	private String country;
	
	/**
	 * @uml.property  name="condition"
	 */
	private String condition;
	
	/**
	 * @uml.property  name="temp"
	 */
	private String temp;
	
	/**
	 * @uml.property  name="chill"
	 */
	private String chill;
	
	/**
	 * @uml.property  name="humidity"
	 */
	private String humidity;
	
	public Weather() {
		
	}

	/**
	 * @return
	 * @uml.property  name="city"
	 */
	public String getCity() {
		return city;
	}

	/**
	 * @param city
	 * @uml.property  name="city"
	 */
	public void setCity(String city) {
		this.city = city;
	}

	/**
	 * @return
	 * @uml.property  name="region"
	 */
	public String getRegion() {
		return region;
	}

	/**
	 * @param region
	 * @uml.property  name="region"
	 */
	public void setRegion(String region) {
		this.region = region;
	}

	/**
	 * @return
	 * @uml.property  name="country"
	 */
	public String getCountry() {
		return country;
	}

	/**
	 * @param country
	 * @uml.property  name="country"
	 */
	public void setCountry(String country) {
		this.country = country;
	}

	/**
	 * @return
	 * @uml.property  name="condition"
	 */
	public String getCondition() {
		return condition;
	}

	/**
	 * @param condition
	 * @uml.property  name="condition"
	 */
	public void setCondition(String condition) {
		this.condition = condition;
	}

	/**
	 * @return
	 * @uml.property  name="temp"
	 */
	public String getTemp() {
		return temp;
	}

	/**
	 * @param temp
	 * @uml.property  name="temp"
	 */
	public void setTemp(String temp) {
		this.temp = temp;
	}

	/**
	 * @return
	 * @uml.property  name="chill"
	 */
	public String getChill() {
		return chill;
	}

	/**
	 * @param chill
	 * @uml.property  name="chill"
	 */
	public void setChill(String chill) {
		this.chill = chill;
	}

	/**
	 * @return
	 * @uml.property  name="humidity"
	 */
	public String getHumidity() {
		return humidity;
	}

	/**
	 * @param humidity
	 * @uml.property  name="humidity"
	 */
	public void setHumidity(String humidity) {
		this.humidity = humidity;
	}
	
}
